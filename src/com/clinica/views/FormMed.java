    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        if  (jTextFieldNome.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha o Nome para continuar", "Cadastrando Médico", JOptionPane.OK_OPTION);
            jTextFieldNome.requestFocus();
        }else{
            if(flag==1){
                mod.setNome(jTextFieldNome.getText());
                mod.setEndereco(jTextFieldEndereco.getText());
                mod.setEspecialidade((String) jComboBoxEspecialidade.getSelectedItem());
                dao.salvar(mod);
                jTextFieldNome.setText("");
                jTextFieldEndereco.setText("");
                jTextFieldNome.setEnabled(false);
                jTextFieldEndereco.setEnabled(false);
                jComboBoxEspecialidade.setEnabled(false);
                jButtonSalvar.setEnabled(false);
                jButtonCancelar.setEnabled(false);
                jButtonEditar.setEnabled(false);
                jButtonExcluir.setEnabled(false);
                jButtonNovo.setEnabled(true);
                jButtonFiltrar.setEnabled(true);
                jTextFieldFiltrar.setEnabled(true);

                preencherTabela("SELECT *FROM medicos ORDER BY nome");
            }else
            {
                mod.setId(Integer.parseInt(jTextFieldId.getText()));
                mod.setNome(jTextFieldNome.getText());
                mod.setEndereco(jTextFieldEndereco.getText());
                mod.setEspecialidade((String) jComboBoxEspecialidade.getSelectedItem());
                dao.editar(mod);
                jButtonSalvar.setEnabled(false);
                jButtonCancelar.setEnabled(false);
                jTextFieldId.setText("");
                jTextFieldNome.setText("");
                jTextFieldEndereco.setText("");
                jButtonFiltrar.setEnabled(true);
                jTextFieldFiltrar.setEnabled(true);

                preencherTabela("SELECT *FROM medicos ORDER BY nome");
            }
    }//GEN-LAST:event_jButtonSalvarActionPerformed
    }
