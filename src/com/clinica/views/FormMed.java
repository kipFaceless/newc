     public void preencherTabela(String Sql){
        ArrayList dados = new ArrayList();
        String [] colunas = new String[]{"Código", "Nome", "Especialidade", "Endereço"};
        
        conex.conexao();
        conex.executaSql(Sql);
        try {
            conex.rs.first();
            do{
                dados.add(new Object[]{conex.rs.getInt("id"), conex.rs.getString("nome"),conex.rs.getString("especialidade"), conex.rs.getString("endereco")});
                
            }while(conex.rs.next());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Procure por Outro Médico");
        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        jTableDentista.setModel(modelo);
        jTableDentista.getColumnModel().getColumn(0).setPreferredWidth(70);
        jTableDentista.getColumnModel().getColumn(0).setResizable(false);
         jTableDentista.getColumnModel().getColumn(1).setPreferredWidth(290);
        jTableDentista.getColumnModel().getColumn(1).setResizable(false);
         jTableDentista.getColumnModel().getColumn(2).setPreferredWidth(180);
        jTableDentista.getColumnModel().getColumn(2).setResizable(false);
         jTableDentista.getColumnModel().getColumn(3).setPreferredWidth(90);
        jTableDentista.getColumnModel().getColumn(3).setResizable(false);
         //jTableDentista.getColumnModel().getColumn(4).setPreferredWidth(90);
       // jTableDentista.getColumnModel().getColumn(4).setResizable(false);
        
        jTableDentista.getTableHeader().setReorderingAllowed(false);
        jTableDentista.setAutoResizeMode(jTableDentista.AUTO_RESIZE_OFF);
        jTableDentista.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conex.desconecta();
    } 
